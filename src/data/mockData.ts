const today = new Date()
const d = (daysFromNow: number) => {
  const dt = new Date(today)
  dt.setDate(dt.getDate() + daysFromNow)
  return dt.toISOString().slice(0, 10)
}
const past = (daysAgo: number) => {
  const dt = new Date(today)
  dt.setDate(dt.getDate() - daysAgo)
  return dt.toISOString().slice(0, 10)
}

